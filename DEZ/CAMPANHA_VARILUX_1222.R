CP_G121_1222 %>% summarize(v=sum(VRVENDA))

CP_G121_1222 %>% summarize(v=sum(BONUS))


## OBS

OBS_G121 <- paste0("RINALDI ","G",CP_G121_1222 %>% 
                     distinct(GCLCODIGO)," ", "VARILUX ", format(floor_date(Sys.Date()-months(1),"month"),"%m%y")) 


## JOIN CPF

LIST_G121_1222 <- inner_join(CP_G121_1222,BASE_CPF,by="CLICODIGO") %>% 
  rename(CPF1=CPF.x) %>% rename(CPF2=CPF.y) %>% 
  .[,-12] %>% 
  rename(.,"CPF"="CPF2") %>% 
  mutate(OBS=OBS_G121)

View(LIST_G121_1222)


### pagamentos

PAG_G121_1222 <- LIST_G121_1222 %>% group_by(CPF) %>% 
  summarize(BONUS=round(sum(BONUS),2)) %>%
  mutate(OBS=OBS_G121) 

View(PAG_G121_1222)

