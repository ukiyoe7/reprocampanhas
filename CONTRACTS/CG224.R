CP_G224_0822 %>% summarize(v=sum(VRVENDA))

CP_G224_0822 %>% summarize(v=sum(BONUS))

## OBSERV

OBS_G224 <- paste0("ITACU ","G",CP_G224_0822 %>% 
                    distinct(GCLCODIGO), " ",format(floor_date(Sys.Date()-months(1),"month"),"%m/%y")) 


## JOIN CPF

LIST_G224_0822  <- inner_join(CP_G224_0822,BASE_CPF,by="CLICODIGO") %>% 
  rename(CPF1=CPF.x) %>% rename(CPF2=CPF.y) %>% 
  .[,-12] %>% 
  rename(.,"CPF"="CPF2") %>% 
  mutate(OBS=OBS_G224)

View(LIST_G224_0822)


### PAY

PAG_G224_0822 <- LIST_G224_0822 %>% group_by(CPF) %>% 
  summarize(BONUS=round(sum(BONUS),2)) %>% 
  mutate(OBS=OBS_G224) 

View(PAG_G224_0822)


PAG_G224_0822 %>% summarize(v=sum(BONUS))


